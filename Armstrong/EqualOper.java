     {
        int num = 153;
        int num1 = 153;
        if(num == num1)
        {
            System.out.println(num+" "+num1+" is Equal");
        }
        char ch = 'a';
        char ch1 = 'a';
        if(ch == ch1)
        {
            System.out.println(ch+" "+ch1+" is Equal");
        }
        else{
            System.out.println(ch+" "+ch1+" is not Equal");
        }
    }
